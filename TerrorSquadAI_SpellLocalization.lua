-- ============================================
-- Función de traducción
-- ============================================
function TerrorSquadAI_TranslateSpell(spanishName)
    if not spanishName then return nil end
    
    -- Si ya está en inglés, devolver tal cual
    local englishName = TerrorSquadAI_SpellDB[spanishName]
    if englishName then
        return englishName
    end
    
    -- Si no se encuentra traducción, devolver el nombre original
    return spanishName
end

-- Función inversa: Inglés -> Español (para debugging)
function TerrorSquadAI_GetSpanishName(englishName)
    if not englishName then return nil end
    
    for spanish, english in pairs(TerrorSquadAI_SpellDB) do
        if english == englishName then
            return spanish
        end
    end
    
    return englishName
end

-- Verificar si el cliente está en español
function TerrorSquadAI_IsSpanishClient()
    local locale = GetLocale()
    return locale == "esES" or locale == "esMX"
end
